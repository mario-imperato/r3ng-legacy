package org.r3.system;

public class ServicePatternException extends Exception
{

	public ServicePatternException()
	{
		super();
	}

	public ServicePatternException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public ServicePatternException(String message)
	{
		super(message);
	}

	public ServicePatternException(Throwable cause)
	{
		super(cause);
	}
     
}
